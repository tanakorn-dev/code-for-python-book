# Reading Input from Keyboard
x = input("What is your name : ")
print(x) # Print input

# Print input with string
# Style 1
print("Hi,",x) # Respond: Hi, ___

# Style 2
print("Hi, " + x) # Respond: Hi, ___

# Style 3
print(f"Hi, {x}") # Respond: Hi, ___

# Style 4
print(("Hi, {0}").format(x)) # Respond: Hi, ___