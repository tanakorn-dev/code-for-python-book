# Python Numbers and Operation
x = 10
y = 2.5

# Change type of number to integers
print(int(x)) # Respond: 10
print(int(y)) # Respond: 2

# Change type of number to floating point
print(float(x)) # Respond: 10.0
print(float(y)) # Respond: 2.5

# Plus two number
print(x + y) # Respond: 12.5

# Minus two number
print(x - y) # Respond: 7.5

# Multiply two number
print(x * y) # Respond: 25.0

# Divide two number
print(x / y) # Respond: 4.0