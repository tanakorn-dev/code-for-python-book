# Python Strings
str = 'Hello World!'

# Prints complete string
print(str) # Respond: Hello World!

# Prints first character of the string
print(str[0]) # Respond: H

# Prints characters starting from 3rd to 5th
print(str[2:5]) # Respond: llo

# Prints string starting from 3rd character
print(str[2:]) # Respond: llo World!

# Prints string two times
print(str * 2) # Respond: Hello World!Hello World!

# Prints concatenated string
print(str + "TEST") # Respond: Hello World!TEST

# Prints count of character in string
print(len(str)) # Respond: 12
