# การหาผลลัพธ์ Boolean
print(10 > 9)   # True
print(10 == 9)  # False
print(10 < 9)   # False

# จงเขียนโปรแกรมเพื่อหาคำตอบต่อไปนี้
x = 10
y = 9

# x เท่ากับ y หรือไม่
print(x == y) # False

# x ไม่เท่ากับ y หรือไม่
print(x != y) # True

# x มากกว่า y หรือไม่
print(x > y) # True

# x น้อยกว่า y หรือไม่
print(x < y) # False

# x มากกว่าหรือเท่ากับ y หรือไม่
print(x >= y) # True

# x น้อยกว่าหรือเท่ากับ y หรือไม่
print(x <= y) # False